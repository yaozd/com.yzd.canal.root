package com.yzd.canal.example.model;

public class TableWithKey {

    private String table;
    private String key;

    public TableWithKey() {
    }

    public String getTable() {
        return table;
    }

    public void setTable(String table) {
        this.table = table;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
